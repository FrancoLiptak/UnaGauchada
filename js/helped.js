$(document).ready(function(){ 
    $('#all').show('slow');
   $('#buttonAll').on('click',function(){
        $('#accepted').hide();
        $('#pending').hide();
        $('#rejected').hide();
        $('#all').show('slow');
   });
   $('#buttonAccepted').on('click',function(){
        $('#all').hide();
        $('#pending').hide();
        $('#rejected').hide();
        $('#accepted').show('slow');
   });
   $('#buttonPending').on('click',function(){
        $('#all').hide();
        $('#accepted').hide();
        $('#rejected').hide();
        $('#pending').show('slow');
   });
   $('#buttonRejected').on('click',function(){
        $('#all').hide();
        $('#pending').hide();
        $('#accepted').hide();
        $('#rejected').show('slow');
   });
});